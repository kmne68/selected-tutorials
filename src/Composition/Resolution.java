package Composition;

/**
 *
 * @author Keith
 */
public class Resolution {
    
    private int width;
    private int height;
    
    public Resolution(int width, int height) {
        
        this.width = width;
        this.height = height;
    }

    /**
     * @return the width
     */
    public int getWidth() {
        return width;
    }

    /**
     * @return the height
     */
    public int getHeight() {
        return height;
    }
    
}
